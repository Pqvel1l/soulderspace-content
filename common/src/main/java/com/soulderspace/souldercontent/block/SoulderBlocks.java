    @Nullable
    public static Identifier getId(Block block) {
        Map.Entry<Identifier, Block> entry = getInstance().queue.entrySet().stream()
                .filter(e -> e.getValue().equals(block)).findFirst().orElse(null);
        if (entry != null) {
            return entry.getKey();
        }
        return null;
    }
